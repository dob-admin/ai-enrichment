#!/usr/bin/env node
// scripts/fetch-taxonomy.js
// Downloads the Shopify Standard Product Taxonomy from GitHub and generates
// src/config/taxonomy.js with a full_name → googleId lookup.
//
// Run: node scripts/fetch-taxonomy.js
// Re-run quarterly when Shopify releases a new taxonomy version.
// Current version: 2026-02

import { writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const OUTPUT_PATH = resolve(__dirname, '../src/config/taxonomy.js')

const TAXONOMY_VERSION = '2026-02'
const TAG = `v${TAXONOMY_VERSION}`
const BASE_URL = `https://raw.githubusercontent.com/Shopify/product-taxonomy/refs/tags/${TAG}/dist/en`

async function fetchText(url) {
  const res = await fetch(url)
  if (!res.ok) throw new Error(`Failed to fetch ${url}: ${res.status}`)
  return res.text()
}

async function fetchJson(url) {
  const res = await fetch(url)
  if (!res.ok) throw new Error(`Failed to fetch ${url}: ${res.status}`)
  return res.json()
}

// Parse categories.txt — format: "gid://shopify/TaxonomyCategory/xxx : Full Path Name"
function parseCategories(txt) {
  const map = {}
  for (const line of txt.split('\n')) {
    const trimmed = line.trim()
    if (!trimmed || !trimmed.startsWith('gid://')) continue
    const colonIdx = trimmed.indexOf(' : ')
    if (colonIdx === -1) continue
    const id = trimmed.slice(0, colonIdx).trim()
    const fullName = trimmed.slice(colonIdx + 3).trim()
    map[id] = fullName
  }
  return map
}

// Build googleId lookup from mapping JSON
// Maps shopify category gid → google category id string
function buildGoogleMap(mappingJson) {
  const googleMap = {}
  for (const mapping of mappingJson.mappings || []) {
    if (!mapping.input_taxonomy?.startsWith('shopify')) continue
    if (!mapping.output_taxonomy?.startsWith('google')) continue
    for (const rule of mapping.rules || []) {
      const shopifyId = rule.input?.category?.id
      const googleIds = rule.output?.category
      if (shopifyId && googleIds?.length) {
        googleMap[shopifyId] = googleIds[0].id
      }
    }
  }
  return googleMap
}

async function main() {
  console.log(`Fetching Shopify taxonomy ${TAXONOMY_VERSION}...`)

  // 1. Fetch categories
  const categoriesTxt = await fetchText(`${BASE_URL}/categories.txt`)
  const categories = parseCategories(categoriesTxt)
  console.log(`  Parsed ${Object.keys(categories).length} categories`)

  // 2. Fetch Google mapping
  const mappingJson = await fetchJson(`${BASE_URL}/integrations/all_mappings.json`)
  const googleMap = buildGoogleMap(mappingJson)
  console.log(`  Parsed ${Object.keys(googleMap).length} Google mappings`)

  // 3. Build full_name → googleId lookup
  const taxonomy = {}
  for (const [gid, fullName] of Object.entries(categories)) {
    taxonomy[fullName] = googleMap[gid] || null
  }

  // 4. Write taxonomy.js
  const js = `// GENERATED — do not edit manually
// Source: Shopify Standard Product Taxonomy ${TAXONOMY_VERSION}
// Generated by: node scripts/fetch-taxonomy.js
// Re-run quarterly when Shopify releases a new taxonomy version.
// https://github.com/Shopify/product-taxonomy

export const TAXONOMY_VERSION = '${TAXONOMY_VERSION}'

// Maps full taxonomy path → Google Shopping category ID
// Shopify Category field stores the full path string (used by Matrixify)
// Google category is derived automatically from the same lookup — no separate Claude call needed
export const TAXONOMY = ${JSON.stringify(taxonomy, null, 2)}
`

  writeFileSync(OUTPUT_PATH, js, 'utf8')
  console.log(`\nWrote ${Object.keys(taxonomy).length} entries to src/config/taxonomy.js`)
  console.log('Done.')
}

main().catch(err => {
  console.error('Error:', err.message)
  process.exit(1)
})
